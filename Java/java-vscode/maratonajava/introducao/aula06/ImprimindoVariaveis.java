package maratonajava.introducao.aula06;

public class ImprimindoVariaveis {
    
    public static void main(String[] args) {
        int idade = 0;
        double salarioDouble = 3000;
        float salarioFloat = 3000;
        byte idadeByte = 12;
        short idadeShort = 32767;
        boolean falso = false;
        long numeroGrande = 1000L;
        char caractere = 'A';
        String nome = "Lucas de Souza Santos";
        
        System.out.println("Variável do tipo int: " + idade);
        System.out.println("Variável do tipo double: " + salarioDouble);
        System.out.println("Variável do tipo float: " + salarioFloat);
        System.out.println("Variável do tipo byte: " + idadeByte);
        System.out.println("Variável do tipo short: " + idadeShort);
        System.out.println("Variável do tipo boolean: " + falso);
        System.out.println("Variável do tipo long: " + numeroGrande);
        System.out.println("Variável do tipo char: " + caractere);
        System.out.println("Variável do tipo String: " + nome);
        
    }
}
